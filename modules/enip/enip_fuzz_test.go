func FuzzEnipHeaderUnMarshal(f *testing.F) {
	f.Add(make([]byte, 24))
	f.Add([]byte{0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00})
	f.Fuzz(func(t *testing.T, data []byte) {
		header := &EnipHeader{}
		_ = header.UnMarshal(data)
	})
}

